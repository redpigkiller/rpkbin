_INIT_PROFILES = ("minimal", "parser", "full", "pty")
_INIT_PROFILE_BLOCKS = {
    "minimal": (),
    "parser": ("parser",),
    "full": ("parser", "memory-parser", "hooks"),
    "pty": ("pty",),
}


def _render_wave_template(name: str, profile: str) -> str:
    try:
        blocks = _INIT_PROFILE_BLOCKS[profile]
    except KeyError as exc:
        allowed = ", ".join(_INIT_PROFILES)
        raise ValueError(f"Unknown init profile {profile!r}. Use one of: {allowed}") from exc
    block_set = set(blocks)
    parser_blocks = []
    attach_lines = []
    
    imports = []
    if "parser" in block_set:
        imports.append("RegexParser")
        parser_blocks.append(_PARSER_TEMPLATE)
        attach_lines.append("job.add_parser(RegexParser(r\"RESULT=(?P<result>\\\\w+)\"))")
    if "memory-parser" in block_set:
        imports.append("StatefulParser")
        parser_blocks.append(_MEMORY_PARSER_TEMPLATE)
        attach_lines.append(
            "job.add_parser(StatefulParser(\n"
            "    r\"(?:STEP=(?P<step>\\\\w+)|RESULT=(?P<result>\\\\w+))\",\n"
            "    on_match=on_step_or_result_match,\n"
            "    to_data=step_or_result_to_data,\n"
            "))"
        )

    hooks = _HOOK_TEMPLATE if "hooks" in block_set else ""
    if hooks:
        attach_lines.append("job.add_hook(Hook(Hook.on_done(), announce_done))")
    pty = _PTY_TEMPLATE if "pty" in block_set else ""
    has_parser = bool(parser_blocks)

    if pty:
        imports.append("os")
    if hooks:
        imports.append("Hook")
    if pty:
        imports.append("PtyJob")
    import_suffix = ""
    if imports:
        stdlib_imports = [item for item in imports if item == "os"]
        wave_imports = [item for item in imports if item != "os"]
        lines = []
        if stdlib_imports:
            lines.append("import os")
        if wave_imports:
            lines.append(f"from rpkbin.wave import {', '.join(wave_imports)}")
        import_suffix = "\n".join(lines)

    return _WAVE_TEMPLATE.substitute(
        name=name,
        imports=import_suffix,
        dashboard_data_columns='    {"label": "Result", "data": "result"},' if has_parser else "",
        command=repr(
            'python -c "print(\'STEP=compile\'); print(\'RESULT=pass\')"'
            if has_parser
            else 'python -c "print(\'Hello from Wave\')"'
        ),
        parser="".join(parser_blocks),
        hooks=hooks,
        attach="\n".join(attach_lines),
        pty=pty,
    )


