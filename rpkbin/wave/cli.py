def _find_wave_docs_src() -> "Path | None":
    """Find the wave-specific docs directory shipped with the package.

    Returns the ``docs/wave`` subdirectory relative to the repository root,
    or ``None`` when the docs tree cannot be located.
    """
    pkg_root = Path(__file__).resolve().parent.parent
    candidates = [
        pkg_root / "docs" / "wave",
        pkg_root.parent / "docs" / "wave",
    ]
    return next((d for d in candidates if d.is_dir()), None)


@main.command("export-docs")
@click.argument("dest", type=click.Path())
@click.option(
    "--force", "-f",
    is_flag=True,
    default=False,
    help="Overwrite the destination directory even if it already contains files.",
)
def cmd_export_docs(dest: str, force: bool) -> None:
    """Export Wave Markdown documentation to the DEST directory.

    Creates DEST if it does not exist. Copies wave.md (English) and wave_zh.md
    (Traditional Chinese) into the 'wave' subdirectory inside DEST.
    Refuses to write when destination already contains files unless --force is given.

    Examples:

      rpk-wave export-docs ./docs

      rpk-wave export-docs ./docs --force
    """
    dest_path = Path(dest)

    docs_src = _find_wave_docs_src()
    if docs_src is None:
        click.echo(
            "[Wave] Cannot find the wave docs directory in the installed package.",
            err=True,
        )
        raise SystemExit(1)

    # Destination: <dest>/wave/
    wave_dest = dest_path / "wave"

    if wave_dest.exists() and any(wave_dest.iterdir()) and not force:
        click.echo(
            f"[Wave] {wave_dest} already has content. Use --force to overwrite.",
            err=True,
        )
        raise SystemExit(1)

    dest_path.mkdir(parents=True, exist_ok=True)
    shutil.copytree(str(docs_src), str(wave_dest), dirs_exist_ok=True)

    click.echo(f"[Wave] Docs exported to: {dest_path.resolve()}")
    for fname in ("wave.md", "wave_zh.md"):
        entry = wave_dest / fname
        if entry.exists():
            rel = entry.relative_to(dest_path)
            click.echo(f"       Main entry:  {rel}")


